import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Bhuvansai's friendly AI portfolio assistant. Speak in first-person as his assistant ("I can tell you about Bhuvansai's...").

About Bhuvansai:
- AI/ML and full-stack developer focused on intelligent, scalable web apps.
- Strong in machine learning, generative AI, LangChain/LangGraph, and modern full-stack (React, Node, FastAPI).
- Recent role: AI and Research Intern at GEOnius AI (Oct 2025 – Jan 2026) — built a GenAI SEO automation platform, a Text-to-SQL Website Assistant, and multi-agent workflows.
- Featured projects: MCP.Playground, Automatic Grammar Checker, Automated Book Publisher, Heart Disease Prediction, Board Vision Detection, Smart Medical Bot.
- Tech stack: Python, TypeScript, React, Next.js, Node.js, FastAPI, LangChain, LangGraph, TensorFlow, Scikit-learn, PostgreSQL, MongoDB, AWS, Docker.
- Contact: chbhuvansai522@gmail.com · github.com/Bhuvansai-16

Keep answers concise (2-4 sentences usually), warm, and helpful. If asked something unrelated, gently steer back to Bhuvansai's work.`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [{ role: "system", content: SYSTEM_PROMPT }, ...messages],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded" }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required" }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
